/*C++ Naming Convention
1. Lower Case
2. Uppercase
3. Camelcase
Rule for naming variables in c++
Rule 01 : Variable names should not begin with a number
Rule 02 : Whitespace is not permitted in variable names
Rule 03 : A C++ keyword(reversed word) cannot be used as a variable name
Rule 04 : it is preferred to use variable names with more than one word with all lowercase letters for the first word and capitalization of the first letter of each subsequent word 
Rule 05 : When creating variables, it is preferred to give them meaningful names{for example : int number Of Apples = 4 }
Rule 06 : All lowercase letters should be used when creating one-word variable names.
*/


/*C++ Identifiers
Names of any class variables methods
Rule 01 : All identifiers should begin with a letter (A to Z or a to z), currency character($) or an underscore(_).
Rule 02 : After the first character, identifiers can have any combination of characters.
Rule 03 :  A keyword cannot be used as an identifier.
Rule 04 : The Identifier are case sensitive.
Rule 05 : Whitespace is not permitted.
Examples of legal and illegal identifiers:
1. 2_abc (Not Allowed)
2. *Tea (Not Allowed)
3. _2abc (Allowed)
4. $Tea (Allowed)
*/

